def _find_modules_v2(
    model,
    ancestor_class: Optional[Set[str]] = None,
    search_class: List[Type[nn.Module]] = [nn.Linear],
    exclude_children_of: Optional[List[Type[nn.Module]]] = [
        LoraInjectedLinear,
    ],
):
    """
    Find all modules of a certain class (or union of classes) that are direct or
    indirect descendants of other modules of a certain class (or union of classes).

    Returns all matching modules, along with the parent of those moduless and the
    names they are referenced by.
    """

    # Get the targets we should replace all linears under
    if ancestor_class is not None:
        ancestors = (
            module
            for module in model.modules()
            if module.__class__.__name__ in ancestor_class
        )
    else:
        # this, incase you want to naively iterate over all modules.
        ancestors = [module for module in model.modules()]

    # For each target find every linear_class module that isn't a child of a LoraInjectedLinear
    for ancestor in ancestors:
        for fullname, module in ancestor.named_modules():
            if any([isinstance(module, _class) for _class in search_class]):
                # Find the direct parent if this is a descendant, not a child, of target
                *path, name = fullname.split(".")
                parent = ancestor
                while path:
                    parent = parent.get_submodule(path.pop(0))
                # Skip this linear if it's a child of a LoraInjectedLinear
                if exclude_children_of and any(
                    [isinstance(parent, _class) for _class in exclude_children_of]
                ):
                    continue
                # Otherwise, yield it
                yield parent, name, module

DEFAULT_TARGET_REPLACE = {"ResidualAttentionBlock"}
_find_modules = _find_modules_v2

class CLIP_RKR(nn.Module):
    def __init__(self,
                 embed_dim: int,
                 # vision
                 image_resolution: int,
                 vision_layers: Union[Tuple[int, int, int, int], int],
                 vision_width: int,
                 vision_patch_size: int,
                 # text
                 context_length: int,
                 vocab_size: int,
                 transformer_width: int,
                 transformer_heads: int,
                 transformer_layers: int,
                 ):
        super().__init__()

        self.context_length = context_length

        if isinstance(vision_layers, (tuple, list)):
            vision_heads = vision_width * 32 // 64
            self.visual = ModifiedResNet(
                layers=vision_layers,
                output_dim=embed_dim,
                heads=vision_heads,
                input_resolution=image_resolution,
                width=vision_width
            )
        else:
            vision_heads = vision_width // 64
            self.visual = VisualTransformer(
                input_resolution=image_resolution,
                patch_size=vision_patch_size,
                width=vision_width,
                layers=vision_layers,
                heads=vision_heads,
                output_dim=embed_dim
            )

        self.transformer = Transformer(
            width=transformer_width,
            layers=transformer_layers,
            heads=transformer_heads,
            attn_mask=self.build_attention_mask()
        )

        self.vocab_size = vocab_size
        self.token_embedding = nn.Embedding(vocab_size, transformer_width)
        self.positional_embedding = nn.Parameter(torch.empty(self.context_length, transformer_width))
        self.ln_final = LayerNorm(transformer_width)

        self.text_projection = nn.Parameter(torch.empty(transformer_width, embed_dim))
        self.logit_scale = nn.Parameter(torch.ones([]))

        self.initialize_parameters()

    def initialize_parameters(self):
        nn.init.normal_(self.token_embedding.weight, std=0.02)
        nn.init.normal_(self.positional_embedding, std=0.01)

        if isinstance(self.visual, ModifiedResNet):
            if self.visual.attnpool is not None:
                std = self.visual.attnpool.c_proj.in_features ** -0.5
                nn.init.normal_(self.visual.attnpool.q_proj.weight, std=std)
                nn.init.normal_(self.visual.attnpool.k_proj.weight, std=std)
                nn.init.normal_(self.visual.attnpool.v_proj.weight, std=std)
                nn.init.normal_(self.visual.attnpool.c_proj.weight, std=std)

            for resnet_block in [self.visual.layer1, self.visual.layer2, self.visual.layer3, self.visual.layer4]:
                for name, param in resnet_block.named_parameters():
                    if name.endswith("bn3.weight"):
                        nn.init.zeros_(param)

        proj_std = (self.transformer.width ** -0.5) * ((2 * self.transformer.layers) ** -0.5)
        attn_std = self.transformer.width ** -0.5
        fc_std = (2 * self.transformer.width) ** -0.5
        for block in self.transformer.resblocks:
            nn.init.normal_(block.attn.in_proj_weight, std=attn_std)
            nn.init.normal_(block.attn.out_proj.weight, std=proj_std)
            nn.init.normal_(block.mlp.c_fc.weight, std=fc_std)
            nn.init.normal_(block.mlp.c_proj.weight, std=proj_std)

        if self.text_projection is not None:
            nn.init.normal_(self.text_projection, std=self.transformer.width ** -0.5)

    def build_attention_mask(self):
        # lazily create causal attention mask, with full attention between the vision tokens
        # pytorch uses additive attention mask; fill with -inf
        mask = torch.empty(self.context_length, self.context_length)
        mask.fill_(float("-inf"))
        mask.triu_(1)  # zero out the lower diagonal
        return mask

    @property
    def dtype(self):
        return self.visual.conv1.weight.dtype

    def encode_image(self, image):
        x, weights = self.visual(image.type(self.dtype))
        return x, weights

    def encode_text(self, text):
        x = self.token_embedding(text).type(self.dtype)  # [batch_size, n_ctx, d_model]

        x = x + self.positional_embedding.type(self.dtype)
        x = x.permute(1, 0, 2)  # NLD -> LND
        x, weights = self.transformer(x)
        x = x.permute(1, 0, 2)  # LND -> NLD
        x = self.ln_final(x).type(self.dtype)

        # x.shape = [batch_size, n_ctx, transformer.width]
        # take features from the eot embedding (eot_token is the highest number in each sequence)
        x = x[torch.arange(x.shape[0]), text.argmax(dim=-1)] @ self.text_projection

        return x, weights

    def forward(self, image, text):
        image_features, _ = self.encode_image(image)
        text_features, _ = self.encode_text(text)

        # normalized features
        image_features = image_features / image_features.norm(dim=-1, keepdim=True)
        text_features = text_features / text_features.norm(dim=-1, keepdim=True)

        # cosine similarity as logits
        logit_scale = self.logit_scale.exp()
        logits_per_image = logit_scale * image_features @ text_features.t()
        logits_per_text = logit_scale * text_features @ image_features.t()

        # shape = [global_batch_size, global_batch_size]
        return logits_per_image, logits_per_text

    def inject_trainable_rkr(
        self,
        target_replace_module: Set[str] = DEFAULT_TARGET_REPLACE,
        r: int = 4,
        loras=None,  # path to lora .pt
        verbose: bool = False,
        dropout_p: float = 0.0,
        woLora: bool = False
    ):
        """
        inject lora into model, and returns lora parameter groups.
        """
        require_grad_params = []
        names = []

        #if loras != None:
        #    loras = torch.load(loras)

        for _module, name, _child_module in _find_modules(
            self, target_replace_module, search_class=[nn.Linear]
        ):
            weight = _child_module.weight
            bias = _child_module.bias
            if verbose:
                print("LoRA Injection : injecting lora into ", name)
                print("LoRA Injection : weight shape", weight.shape)
            
            if not woLora:
                _tmp = LoraInjectedLinear(
                    _child_module.in_features,
                    _child_module.out_features,
                    _child_module.bias is not None,
                    r=r,
                    dropout_p=dropout_p,
                )
                _tmp.linear.weight = weight
                if bias is not None:
                    _tmp.linear.bias = bias

                # switch the module
                _tmp.to(_child_module.weight.device).to(_child_module.weight.dtype)
                _module._modules[name] = _tmp

                require_grad_params.append(_module._modules[name].lora_up.parameters())
                require_grad_params.append(_module._modules[name].lora_down.parameters())
                require_grad_params.append(_module._modules[name].scale)

                if loras != None:
                    _module._modules[name].lora_up.weight = torch.nn.Parameter(loras.pop(0))
                    _module._modules[name].lora_down.weight = torch.nn.Parameter(loras.pop(0))
                    _module._modules[name].scale = torch.nn.Parameter(loras.pop(0))

                _module._modules[name].lora_up.weight.requires_grad = True
                _module._modules[name].lora_down.weight.requires_grad = True
                _module._modules[name].scale.requires_grad = True
                names.append(name)
            else:
                _tmp = WoLoraInjectedLinear(
                    _child_module.in_features,
                    _child_module.out_features,
                    _child_module.bias is not None,
                    r=r,
                    dropout_p=dropout_p,
                )
                _tmp.linear.weight = weight
                if bias is not None:
                    _tmp.linear.bias = bias

                # switch the module
                _tmp.to(_child_module.weight.device).to(_child_module.weight.dtype)
                _module._modules[name] = _tmp

                require_grad_params.append(_module._modules[name].scale)

                if loras != None:
                    _module._modules[name].scale = torch.nn.Parameter(loras.pop(0))

                _module._modules[name].scale.requires_grad = True
                names.append(name)

        return require_grad_params, names


    def extract_lora_ups_down(self, target_replace_module=DEFAULT_TARGET_REPLACE):

        loras = []

        try:
            for _m, _n, _child_module in _find_modules(
                self,
                target_replace_module,
                search_class=[LoraInjectedLinear],
            ):
                loras.append((_child_module.lora_up, _child_module.lora_down, _child_module.scale))
        except:
            for _m, _n, _child_module in _find_modules(
                self,
                target_replace_module,
                search_class=[WoLoraInjectedLinear],
            ):
                loras.append((_child_module.scale))      

        if len(loras) == 0:
            raise ValueError("No lora injected.")

        return loras

    def save_lora_weight(
        self,
        target_replace_module=DEFAULT_TARGET_REPLACE,
    ):
        weights = []
        try:
            for _up, _down, _scale in self.extract_lora_ups_down(
                target_replace_module=target_replace_module
            ):  
                weights.append(_up.weight.to("cpu")) #.to(torch.float16)
                weights.append(_down.weight.to("cpu")) #.to(torch.float16)
                weights.append(_scale.to("cpu"))
        except:
            for _scale in self.extract_lora_ups_down(
                target_replace_module=target_replace_module
            ):
                weights.append(_scale.to("cpu"))           

        #torch.save(weights, path)
        return weights